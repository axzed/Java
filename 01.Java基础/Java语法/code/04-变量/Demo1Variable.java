public class Demo1Variable {
	/*
		基本数据类型：
			
			整数：
					byte		1个字节
					short		2个字节
					int			4个字节
					long		8个字节
					
			小数：
					float		4个字节
					double		8个字节
					
			字符：
					char		2个字节
					
			布尔：
					boolean		1个字节
	*/
	public static void main(String[] args) {
		// 格式：数据类型 变量名 = 数据值;
		
		// 整数类型变量
		int a = 10;
		System.out.println(a);
		
		// 小数类型变量
		double b = 12.3;
		System.out.println(b);
		
		// 字符类型变量
		char c = 'a';
		System.out.println(c);
		
		// 布尔类型变量
		boolean d = true;
		System.out.println(d);
	}
}