package com.itheima.shortcut;

public class Demo2 {
    /*
        Ctrl + alt + L          格式化代码
        Alt + enter            代码修正提示
        Ctrl + D                向下复制一行
        Ctrl + X                剪切当前行
        Ctrl + /                批量加入单行注释，再按一次就是取消
        Ctrl + shift + /        批量加入多行注释，再按一次就是取消
        Alt + shift + ↑         上移当前行
        Alt + shift + ↓         下移当前行
     */
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        int a = 10;
        byte b = (byte) a;

        int c = 10;
        System.out.println(c);
    }
}
